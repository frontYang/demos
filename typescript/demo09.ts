// 不传参数
let d:Date = new Date()
console.log(d)

// 传递一个整数
let d2:Date = new Date(1000)
let d3:Date = new Date(2000)
console.log(d2)
console.log(d3)


// 传递一个字符串
let d4:Date = new Date('2019/10/06 12:52:00')
let d5:Date = new Date('2019-10-06 12:52:00')
let d6:Date = new Date('2019-10-06T12:52:00')
console.log(d4)
console.log(d5)
console.log(d6)
